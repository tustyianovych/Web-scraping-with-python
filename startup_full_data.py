class Startups(scrapy.Spider):
    
    name = 'startup'
    start_urls = urls
        
    def parse(self, response):
        startup = dict()
        startup['company_url'] = response.xpath('//div[@class="mbt"]/span[1]/a/@href').extract()
        startup['name'] = response.xpath('normalize-space(//h1[@class="profile-startup"]/text())').extract()
        startup['description'] = response.xpath('normalize-space(//p[@class="profile-desc-text"]/text())').extract()
        startup['web-site'] = response.xpath('normalize-space(//div[@class="mbt"]/span/a/@href)').extract()
        startup['location']= response.xpath('//div[@class="mbt"]/span[3]/a/text()').extract()
        startup['tags']= response.xpath('normalize-space(//div[contains(@style, "word-wrap: break-word;")])').extract()
        startup['profile'] = response.xpath('//div[contains(@style, "font-size:16px;")]/text()').extract()
        startup['urls'] = response.xpath('//div[@class="col-md-5 socials pdt text-right "]/a/@href').extract()
        startup['founded'] = response.xpath('//div[@class="col-md-12"]/p/span/text()').extract()
        return(startup)

